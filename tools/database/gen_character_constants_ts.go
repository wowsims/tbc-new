package database

import (
	"bytes"
	"fmt"
	"maps"
	"os"
	"slices"
	"text/template"

	"github.com/wowsims/tbc/sim/core"
	"github.com/wowsims/tbc/sim/core/proto"
)

type tsCharacterConstantsEligibleWeaponType struct {
	WeaponTypeName string
	CanUseTwoHand  bool
}

type tsCharacterConstantsClassCapability struct {
	ClassName            string
	ArmorTypeNames       []string
	WeaponTypes          []tsCharacterConstantsEligibleWeaponType
	RangedWeaponTypeName []string
	RaceNames            []string
}

type tsCharacterConstants struct {
	ClassCapabilities []tsCharacterConstantsClassCapability
	SpecCanDualWield  []string
}

const characterConstantsTSTemplate = `// Code generated by tools/database/gen_db/gen_character_constants_ts.go. DO NOT EDIT.

import { ArmorType, Class, Race, RangedWeaponType, Spec, WeaponType } from '../proto/common';

export type EligibleWeaponType = {
	weaponType: WeaponType;
	canUseTwoHand: boolean;
};

export const CLASS_ARMOR_TYPES: Record<number, ArmorType[]> = {
{{- range .ClassCapabilities }}
	[Class.{{ .ClassName }}]: [{{- range $index, $armorTypeName := .ArmorTypeNames }}{{ if $index }}, {{ end }}ArmorType.{{ $armorTypeName }}{{- end }}],
{{- end }}
};

export const CLASS_WEAPON_TYPES: Record<number, EligibleWeaponType[]> = {
{{- range .ClassCapabilities }}
	[Class.{{ .ClassName }}]: [{{- range $index, $weaponType := .WeaponTypes }}{{ if $index }}, {{ end }}{ weaponType: WeaponType.{{ $weaponType.WeaponTypeName }}, canUseTwoHand: {{ $weaponType.CanUseTwoHand }} }{{- end }}],
{{- end }}
};

export const CLASS_RANGED_WEAPON_TYPES: Record<number, RangedWeaponType[]> = {
{{- range .ClassCapabilities }}
	[Class.{{ .ClassName }}]: [{{- range $index, $rangedWeaponTypeName := .RangedWeaponTypeName }}{{ if $index }}, {{ end }}RangedWeaponType.{{ $rangedWeaponTypeName }}{{- end }}],
{{- end }}
};

export const CLASS_RACES: Record<number, Race[]> = {
{{- range .ClassCapabilities }}
	[Class.{{ .ClassName }}]: [{{- range $index, $raceName := .RaceNames }}{{ if $index }}, {{ end }}Race.{{ $raceName }}{{- end }}],
{{- end }}
};

export const SPEC_CAN_DUAL_WIELD = new Set<Spec>([
{{- range .SpecCanDualWield }}
	Spec.{{ . }},
{{- end }}
]);
`

func GenerateCharacterConstantsTSFile() error {
	classArmorTypes := core.ClassArmorTypeCapabilities
	classWeaponTypes := core.ClassWeaponTypeCapabilities
	classRangedWeaponTypes := core.ClassRangedWeaponTypeCapabilities
	classRaces := core.ClassRaceCapabilities
	specCanDualWield := core.SpecCanDualWieldCapabilities

	classIDs := slices.Sorted(maps.Keys(classArmorTypes))

	classCapabilities := make([]tsCharacterConstantsClassCapability, 0, len(classIDs))
	for _, classID := range classIDs {
		classIDValue := int32(classID)

		armorTypes := classArmorTypes[classID]
		armorTypeNames := make([]string, 0, len(armorTypes))
		for _, armorType := range armorTypes {
			armorTypeNames = append(armorTypeNames, proto.ArmorType_name[int32(armorType)])
		}

		weaponMap := classWeaponTypes[classID]
		weaponTypeIDs := slices.Sorted(maps.Keys(weaponMap))
		weaponTypes := make([]tsCharacterConstantsEligibleWeaponType, 0, len(weaponTypeIDs))
		for _, weaponType := range weaponTypeIDs {
			weaponTypeID := int32(weaponType)
			weaponTypes = append(weaponTypes, tsCharacterConstantsEligibleWeaponType{
				WeaponTypeName: proto.WeaponType_name[weaponTypeID],
				CanUseTwoHand:  weaponMap[weaponType].CanUseTwoHand,
			})
		}

		rangedWeaponTypes := classRangedWeaponTypes[classID]
		rangedWeaponTypeNames := make([]string, 0, len(rangedWeaponTypes))
		for _, rangedWeaponType := range rangedWeaponTypes {
			rangedWeaponTypeNames = append(rangedWeaponTypeNames, proto.RangedWeaponType_name[int32(rangedWeaponType)])
		}

		races := classRaces[classID]
		raceNames := make([]string, 0, len(races))
		for _, race := range races {
			raceNames = append(raceNames, proto.Race_name[int32(race)])
		}

		classCapabilities = append(classCapabilities, tsCharacterConstantsClassCapability{
			ClassName:            proto.Class_name[classIDValue],
			ArmorTypeNames:       armorTypeNames,
			WeaponTypes:          weaponTypes,
			RangedWeaponTypeName: rangedWeaponTypeNames,
			RaceNames:            raceNames,
		})
	}

	specIDs := make([]proto.Spec, 0, len(specCanDualWield))
	for specID, canDualWield := range specCanDualWield {
		if !canDualWield {
			continue
		}
		specIDs = append(specIDs, specID)
	}
	slices.Sort(specIDs)

	specNames := make([]string, 0, len(specIDs))
	for _, specID := range specIDs {
		specNames = append(specNames, proto.Spec_name[int32(specID)])
	}

	tpl, err := template.New("characterConstantsTS").Parse(characterConstantsTSTemplate)
	if err != nil {
		return fmt.Errorf("failed to parse character constants TS template: %w", err)
	}

	var buf bytes.Buffer
	if err := tpl.Execute(&buf, tsCharacterConstants{
		ClassCapabilities: classCapabilities,
		SpecCanDualWield:  specNames,
	}); err != nil {
		return fmt.Errorf("failed to execute character constants TS template: %w", err)
	}

	if err := os.WriteFile("ui/core/player_classes/capabilities_auto_gen.ts", buf.Bytes(), 0644); err != nil {
		return fmt.Errorf("failed to write generated capabilities TS file: %w", err)
	}

	return nil
}
